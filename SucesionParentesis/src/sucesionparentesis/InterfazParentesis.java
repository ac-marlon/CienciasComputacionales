    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        contador = 0;
        banderaPaso = true;
        sucesionParentesis = jTextField1.getText();
        /*Insertamos datos en la pila*/
        for (int i = 0; i < sucesionParentesis.length(); i++) {
            if (sucesionParentesis.charAt(i) == '(' || sucesionParentesis.charAt(i) == ')') {
                pilaParentesis.push(sucesionParentesis.charAt(i));
            } else {
                banderaPaso = false;
                jTextField2.setText("Â¡La entrada es invalida, solo se aceptan parentesis!");
                break;
            }
        }

        /*Estructura repetitiva para desapilar*/
        if (banderaPaso == true) {
            //System.out.println("Pila: " + pilaParentesis);
            while (!pilaParentesis.empty()) {
                if (pilaParentesis.peek() == '(') {
                    contador++;
                } else {
                    contador--;
                }
                pilaParentesis.pop();
            }
            if (contador == 0) {
                jTextField2.setText("La sucesion esta equilibrada");
            } else {
                jTextField2.setText("No hay equilibrio en la entrada de parentesis");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

